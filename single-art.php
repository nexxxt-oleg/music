            <h2>Использование манометров на баллонах с опасными газами</h2>
            <p>Для точного контроля остаточного давления в баллонах с кислородом, ацетиленом и другими взрывоопасными
                газами и смесями используют специально разработанные манометры, которые инженеры называют
                «кислородными». Такие манометры отличаются тем, что в процессе производства они проходят специальную
                технологическую процедуру очистки сухим воздухом. С их внутренних частей, которые вступают в
                непосредственный контакт с кислородом, удаляют все масло и смазку, оставшиеся после механической
                обработки. Иначе кислород, вступив в контакт с жирами или маслами, быстро окислит их, смесь
                воспламенится и разрушит манометр.</p>
